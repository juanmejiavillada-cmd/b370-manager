def get_variaciones(pid):
    r = requests.get(f"{API_BASE}/products/{pid}/variations", auth=AUTH, params={"per_page":100})
    return r.json() if r.status_code == 200 else []

def update_stock(pid, vid, qty):
    r = requests.put(f"{API_BASE}/products/{pid}/variations/{vid}", auth=AUTH,
        json={"manage_stock":True,"stock_quantity":qty,"stock_status":"instock" if qty > 0 else "outofstock"})
    return r.status_code in (200,201)

def main():
    print("="*55)
    print("  B370 — ACTUALIZACIÓN DE INVENTARIO DESDE QUENTI")
    print("="*55)
    total_ok = total_err = sin_sku = 0
    for pid in PRODUCT_IDS:
        print(f"\n🟡 Producto ID {pid}")
        for v in get_variaciones(pid):
            sku = v.get("sku","").strip()
            vid = v["id"]
            if not sku or sku not in STOCK_MAP:
                sin_sku += 1; continue
            qty = STOCK_MAP[sku]
            ok = update_stock(pid, vid, qty)
            talla = next((a["option"] for a in v.get("attributes",[]) if a["name"]=="Tallas"),"?")
            tipo  = next((a["option"] for a in v.get("attributes",[]) if a["name"]!="Tallas"),"")
            label = f"{tipo}/{talla}" if tipo else talla
            print(f"   {'✅' if ok else '❌'} {label} → {qty} uds")
            if ok: total_ok += 1
            else: total_err += 1
            time.sleep(0.3)
    print(f"\n  ✅ {total_ok} variaciones actualizadas")
    if total_err: print(f"  ❌ {total_err} errores")
    if sin_sku: print(f"  ⚠️  {sin_sku} sin SKU")
    input("\n  Presiona ENTER para cerrar...")

if __name__ == "__main__":
    main()
